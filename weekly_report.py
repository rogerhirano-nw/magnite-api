"""
Weekly deal health report — emails a list of deals that were never accepted
by the buyer (bid_responses = 0 across all history).

Two categories:
  1. Never sent     — bid_requests = 0 (trafficking issue)
  2. Never accepted — bid_requests > 0, bid_responses = 0 (buyer hasn't accepted)

Run manually:  python weekly_report.py
Run on a cron: GitHub Actions weekly_report.yml
Test sender:   python weekly_report.py --test
"""

from __future__ import annotations

import argparse
import os
from datetime import date
from pathlib import Path

from agentmail import AgentMail

import pandas as pd
import sqlalchemy


# ── env / config ──────────────────────────────────────────────────────────────

def _load_dotenv() -> None:
    env_file = Path(__file__).parent / ".env"
    if not env_file.exists():
        return
    for line in env_file.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        k, v = line.split("=", 1)
        os.environ.setdefault(k.strip(), v.strip().strip('"').strip("'"))


